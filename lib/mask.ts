/* ============================================================
   OfferMate · 本地脱敏（内测隐私）
   发送给 LLM / 写入 sessionStorage 前，在浏览器里遮蔽邮箱 / 手机 / 长数字串。
   名字不做正则去除（无 NER，易把正文也毁掉）——故标签只承诺「邮箱 / 电话」。
   纯函数、幂等。仅在诊断页 anon 开关打开时调用。
   ============================================================ */

export function maskPII(text: string): string {
  if (!text) return text;
  return text
    // 邮箱
    .replace(/[\w.+-]+@[\w.-]+\.[A-Za-z]{2,}/g, "[email]")
    // ≥13 位裸数字串先走（身份证 18 / 银行卡 16-19）——抢在手机规则前，避免长串被切成碎片
    .replace(/\b\d{13,}\b/g, "[number]")
    // 中国大陆手机号：可带 +86 / 分隔符；1[3-9] 开头共 11 位（避开日期/GPA/学号）
    .replace(/(?:\+?86[\s-]?)?1[3-9]\d[\s-]?\d{4}[\s-]?\d{4}/g, "[phone]");
}
